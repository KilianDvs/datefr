exports.isValidMonth = function (month) {
	const validMonths = [
		"jan",
		"janv",
		"janvier",
		"january",
		"fev",
		"feb",
		"fevrier",
		"février",
		"february",
		"mar",
		"mars",
		"march",
		"avr",
		"apr",
		"avril",
		"april",
		"mai",
		"may",
		"jun",
		"juin",
		"june",
		"jul",
		"juillet",
		"july",
		"aug",
		"aout",
		"août",
		"august",
		"sep",
		"sept",
		"septembre",
		"september",
		"oct",
		"octobre",
		"october",
		"nov",
		"novembre",
		"november",
		"dec",
		"decembre",
		"décembre",
		"december",
	]
	return !isNaN(+month)
		? +month >= 1 && +month <= 12 && Number.isInteger(+month)
		: validMonths.includes(month.toLowerCase())
}
